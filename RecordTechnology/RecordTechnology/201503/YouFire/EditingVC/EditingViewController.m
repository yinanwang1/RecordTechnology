- (void)carousel:(__unused iCarousel *)carousel didSelectItemAtIndex:(NSInteger)index
{
    if ( index >= 0 && index < [self.items count] )
    {
        NSNumber *item = [self.items objectAtIndex:index];

        NSLog(@"Tapped view number: %@", item);
    }
}

